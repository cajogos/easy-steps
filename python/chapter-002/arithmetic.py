a = 8
b = 2

print('Addition:\t\t', a, '+', b, '=', a + b)

print('Subtraction:\t', a, '-', b, '=', a - b)

print('Multiplication:\t', a, 'x', b, '=', a * b)

print('Division:\t\t', a, '/', b, '=', a / b)

print('Floor Division:\t', a, '//', b, '=', a // b)

print('Modulus:\t\t', a, '%', b, '=', a % b)

print('Exponent:\t\t ', a, '** = ', a ** b, sep = '')
